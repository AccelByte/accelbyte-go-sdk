package clients

import (
	"github.com/AccelByte/accelbyte-go-sdk/iam-sdk/pkg/iamclient/clients"
	"github.com/AccelByte/accelbyte-go-sdk/services-api/pkg/factory"
	"github.com/AccelByte/accelbyte-go-sdk/services-api/pkg/service/iam"
	"github.com/AccelByte/sample-apps/cmd"
	"github.com/AccelByte/sample-apps/pkg/repository"
	"github.com/sirupsen/logrus"
	"github.com/spf13/cobra"
)

// getClientCmd represents the getClient command
var getClientCmd = &cobra.Command{
	Use:   "getClient",
	Short: "Get client",
	Long:  `Get client`,
	RunE: func(cmd *cobra.Command, args []string) error {
		clientsService := &iam.ClientsService{
			Client:          factory.NewIamClient(&repository.ConfigRepositoryImpl{}),
			TokenRepository: &repository.TokenRepositoryImpl{},
		}
		clientId, _ := cmd.Flags().GetString("clientId")
		input := &clients.GetClientParams{
			ClientID: clientId,
		}
		//lint:ignore SA1019 Ignore the deprecation warnings
		ok, err := clientsService.GetClient(input)
		logrus.Infof("Response %v", ok)
		if err != nil {
			logrus.Error(err)
			return err
		}
		return nil
	},
}

func init() {
	cmd.RootCmd.AddCommand(getClientCmd)
	getClientCmd.Flags().StringP("clientId", "cd", " ", "Client id")
	_ = getClientCmd.MarkFlagRequired("clientId")
}
