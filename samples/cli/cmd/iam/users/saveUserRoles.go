package cmd

import (
	"encoding/json"
	"github.com/AccelByte/accelbyte-go-sdk/iam-sdk/pkg/iamclient/users"
	"github.com/AccelByte/accelbyte-go-sdk/services-api/pkg/factory"
	"github.com/AccelByte/accelbyte-go-sdk/services-api/pkg/service/iam"
	"github.com/AccelByte/sample-apps/cmd"
	"github.com/AccelByte/sample-apps/pkg/repository"
	"github.com/sirupsen/logrus"
	"github.com/spf13/cobra"
)

// saveUserRolesCmd represents the saveUserRoles command
var saveUserRolesCmd = &cobra.Command{
	Use:   "saveUserRoles",
	Short: "Save user roles",
	Long:  `Save user roles`,
	RunE: func(cmd *cobra.Command, args []string) error {
		usersService := &iam.UsersService{
			Client:          factory.NewIamClient(&repository.ConfigRepositoryImpl{}),
			TokenRepository: &repository.TokenRepositoryImpl{},
		}
		bodyString := cmd.Flag("body").Value.String()
		var body []string
		errBody := json.Unmarshal([]byte(bodyString), &body)
		if errBody != nil {
			return errBody
		}
		namespace, _ := cmd.Flags().GetString("namespace")
		userId, _ := cmd.Flags().GetString("userId")
		input := &users.SaveUserRolesParams{
			Body:      body,
			Namespace: namespace,
			UserID:    userId,
		}
		//lint:ignore SA1019 Ignore the deprecation warnings
		errInput := usersService.SaveUserRoles(input)
		if errInput != nil {
			logrus.Error(errInput)
			return errInput
		}
		return nil
	},
}

func init() {
	cmd.RootCmd.AddCommand(saveUserRolesCmd)
	saveUserRolesCmd.Flags().StringP("body", "b", " ", "Body")
	_ = saveUserRolesCmd.MarkFlagRequired("body")
	saveUserRolesCmd.Flags().StringP("namespace", "n", " ", "Namespace")
	_ = saveUserRolesCmd.MarkFlagRequired("namespace")
	saveUserRolesCmd.Flags().StringP("userId", "u", " ", "User id")
	_ = saveUserRolesCmd.MarkFlagRequired("userId")
}
