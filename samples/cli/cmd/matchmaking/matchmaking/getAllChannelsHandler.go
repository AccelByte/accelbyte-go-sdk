package cmd

import (
	matchmaking_ "github.com/AccelByte/accelbyte-go-sdk/matchmaking-sdk/pkg/matchmakingclient/matchmaking"
	"github.com/AccelByte/accelbyte-go-sdk/services-api/pkg/factory"
	"github.com/AccelByte/accelbyte-go-sdk/services-api/pkg/service/matchmaking"
	"github.com/AccelByte/sample-apps/cmd"
	"github.com/AccelByte/sample-apps/pkg/repository"
	"github.com/sirupsen/logrus"
	"github.com/spf13/cobra"
)

// getAllChannelsHandlerCmd represents the getAllChannelsHandler command
var getAllChannelsHandlerCmd = &cobra.Command{
	Use:   "getAllChannelsHandler",
	Short: "Get all channels handler",
	Long:  `Get all channels handler`,
	RunE: func(cmd *cobra.Command, args []string) error {
		matchmakingService := &matchmaking.MatchmakingService{
			Client:          factory.NewMatchmakingClient(&repository.ConfigRepositoryImpl{}),
			TokenRepository: &repository.TokenRepositoryImpl{},
		}
		namespace, _ := cmd.Flags().GetString("namespace")
		limit, _ := cmd.Flags().GetInt64("limit")
		offset, _ := cmd.Flags().GetInt64("offset")
		input := &matchmaking_.GetAllChannelsHandlerParams{
			Namespace: namespace,
			Limit:     &limit,
			Offset:    &offset,
		}
		//lint:ignore SA1019 Ignore the deprecation warnings
		ok, err := matchmakingService.GetAllChannelsHandler(input)
		logrus.Infof("Response %v", ok)
		if err != nil {
			logrus.Error(err)
			return err
		}
		return nil
	},
}

func init() {
	cmd.RootCmd.AddCommand(getAllChannelsHandlerCmd)
	getAllChannelsHandlerCmd.Flags().StringP("namespace", "n", " ", "Namespace")
	_ = getAllChannelsHandlerCmd.MarkFlagRequired("namespace")
	getAllChannelsHandlerCmd.Flags().Int64P("limit", "l", 20, "Limit")
	getAllChannelsHandlerCmd.Flags().Int64P("offset", "o", 0, "Offset")
}
