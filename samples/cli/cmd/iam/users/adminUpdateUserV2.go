package cmd

import (
	"encoding/json"
	"github.com/AccelByte/accelbyte-go-sdk/iam-sdk/pkg/iamclient/users"
	"github.com/AccelByte/accelbyte-go-sdk/iam-sdk/pkg/iamclientmodels"
	"github.com/AccelByte/accelbyte-go-sdk/services-api/pkg/factory"
	"github.com/AccelByte/accelbyte-go-sdk/services-api/pkg/service/iam"
	"github.com/AccelByte/sample-apps/cmd"
	"github.com/AccelByte/sample-apps/pkg/repository"
	"github.com/sirupsen/logrus"
	"github.com/spf13/cobra"
)

// adminUpdateUserV2Cmd represents the adminUpdateUserV2 command
var adminUpdateUserV2Cmd = &cobra.Command{
	Use:   "adminUpdateUserV2",
	Short: "Admin update user V2",
	Long:  `Admin update user V2`,
	RunE: func(cmd *cobra.Command, args []string) error {
		usersService := &iam.UsersService{
			Client:          factory.NewIamClient(&repository.ConfigRepositoryImpl{}),
			TokenRepository: &repository.TokenRepositoryImpl{},
		}
		bodyString := cmd.Flag("body").Value.String()
		var body *iamclientmodels.ModelUserUpdateRequest
		errBody := json.Unmarshal([]byte(bodyString), &body)
		if errBody != nil {
			return errBody
		}
		namespace, _ := cmd.Flags().GetString("namespace")
		userId, _ := cmd.Flags().GetString("userId")
		input := &users.AdminUpdateUserV2Params{
			Body:      body,
			Namespace: namespace,
			UserID:    userId,
		}
		//lint:ignore SA1019 Ignore the deprecation warnings
		ok, err := usersService.AdminUpdateUserV2(input)
		logrus.Infof("Response %v", ok)
		if err != nil {
			logrus.Error(err)
			return err
		}
		return nil
	},
}

func init() {
	cmd.RootCmd.AddCommand(adminUpdateUserV2Cmd)
	adminUpdateUserV2Cmd.Flags().StringP("body", "b", " ", "Body")
	_ = adminUpdateUserV2Cmd.MarkFlagRequired("body")
	adminUpdateUserV2Cmd.Flags().StringP("namespace", "n", " ", "Namespace")
	_ = adminUpdateUserV2Cmd.MarkFlagRequired("namespace")
	adminUpdateUserV2Cmd.Flags().StringP("userId", "u", " ", "User id")
	_ = adminUpdateUserV2Cmd.MarkFlagRequired("userId")
}
